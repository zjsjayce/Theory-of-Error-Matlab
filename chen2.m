function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global D1;
n=length(D1);%剔除完粗大误差的数据个数
av=mean(D1);%算术平均值
set(handles.edit3,'string',num2str(av));
s=std(D1);%标准差
set(handles.edit4,'string',num2str(s));
slim=3*s;%极限误差
set(handles.edit5,'string',num2str(slim));
ss=s/sqrt(n);%算术平均值的标准差
set(handles.edit6,'string',num2str(ss));
sslim=3*ss;%算术平均值的极限误差
set(handles.edit7,'string',num2str(sslim));
r1=av-3*sslim;%测量结果左限
r2=av+3*sslim;%测量结果右限
r=[r1,r2];
set(handles.edit8,'string',num2str(r));
